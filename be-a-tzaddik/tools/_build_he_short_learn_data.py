PREFIX_HE.update(PREFIX_HE_EXTRA)

MELECHA_KEY_RE = re.compile(
    r"Learn about (?:the )?(?:little known )?[Mm]elacha of ([^—!]+?)(?:—|!|$)"
)
MELECHA_ALT_RE = re.compile(r"Learn about ([A-Z][A-Za-z' ]+?) —")


def ensure_min_len(text: str, min_len: int = MIN_HE_LEN) -> str:
    out = text.strip()
    while len(out) < min_len:
        out += HE_PAD
    return out


def melacha_name(key: str) -> str | None:
    m = MELECHA_KEY_RE.search(key)
    if m:
        name = m.group(1).strip()
        if name in MELECHA_HE:
            return name
    m = MELECHA_ALT_RE.search(key)
    if m:
        name = m.group(1).strip()
        if name in MELECHA_HE:
            return name
    return None


def build_exact() -> dict[str, str]:
    out: dict[str, str] = {}
    for key in KEYS:
        name = melacha_name(key)
        if name:
            out[key] = ensure_min_len(MELECHA_HE[name])
            continue
        for prefix, val in sorted(PREFIX_HE.items(), key=lambda x: len(x[0]), reverse=True):
            if key.startswith(prefix):
                out[key] = ensure_min_len(val)
                break
    return out


def main() -> None:
    exact = build_exact()
    missing = [k for k in KEYS if k not in exact]
    print(f"built {len(exact)}/{len(KEYS)}; missing {len(missing)}")
    for k in missing[:20]:
        print(" ?", k[:70])

    lines = [
        '"""Hebrew expansions for short Learn-about Mitzvah Me alerts (400+ chars)."""',
        "",
        "from __future__ import annotations",
        "",
        "HE_SHORT_LEARN: dict[str, str] = {",
    ]
    for k, v in sorted(exact.items(), key=lambda x: x[0][:40]):
        lines.append(f"    {json.dumps(k, ensure_ascii=False)}: (")
        lines.append(f"        {json.dumps(v, ensure_ascii=False)}")
        lines.append("    ),")
    lines.append("}")
    lines.append("")
    prefix_expand: dict[str, str] = {}
    for p, v in PREFIX_HE.items():
        prefix_expand[p] = ensure_min_len(v)
    for name, v in MELECHA_HE.items():
        for pref in (
            f"Learn about the Melacha of {name}",
            f"Learn about the melacha of {name}",
            f"Learn about {name} —",
        ):
            if pref not in prefix_expand:
                prefix_expand[pref] = ensure_min_len(v)

    lines.append("PREFIX_HE_SHORT_EXPAND: dict[str, str] = {")
    for p, v in sorted(prefix_expand.items(), key=lambda x: len(x[0]), reverse=True):
        lines.append(f"    {json.dumps(p, ensure_ascii=False)}: (")
        lines.append(f"        {json.dumps(v, ensure_ascii=False)}")
        lines.append("    ),")
    lines.append("}")
    lines.append("")

    out_path = Path(__file__).parent / "_he_short_learn_expansions_data.py"
    out_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"wrote {out_path.name}")


if __name__ == "__main__":
    main()
