def apply_ru_ty(text: str) -> str:
    """Convert formal вы to ты in direct-address UI copy; preserve « » quotes."""
    protected: list[str] = []

    def prot(m: re.Match[str]) -> str:
        protected.append(m.group(0))
        return f"__PROT{len(protected) - 1}__"

    text = re.sub(r"«[^»]*»", prot, text)
    for old, new in _RU_TY_BOUNDED:
        text = re.sub(_CYR_LOOKBEHIND + re.escape(old), new, text)
    for old, new in _RU_TY_PAIRS:
        text = text.replace(old, new)
    for old, new in _RU_TY_POST:
        text = text.replace(old, new)
    for i, chunk in enumerate(protected):
        text = text.replace(f"__PROT{i}__", chunk)
    return text
