def nine_days_ru_for_key(key: str) -> str | None:
    best: str | None = None
    best_len = 0
    for prefix, val in NINE_DAYS_RU_BY_PREFIX.items():
        if key.startswith(prefix) and len(prefix) > best_len:
            best = val
            best_len = len(prefix)
    return best
