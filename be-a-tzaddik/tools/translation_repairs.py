def _normalize_spacing(text: str) -> str:
    text = re.sub(r"  +", " ", text)
    text = re.sub(r" +\.\.\.", "...", text)
    text = re.sub(r"\.\.\. +", "... ", text)
    text = re.sub(r" +\n", "\n", text)
    text = re.sub(r"\n +", "\n", text)
    return text


def _repair_he_placeholders(text: str) -> str:
    """Space between Hebrew text and $template placeholders."""
    text = re.sub(r"([\u0590-\u05ff])(\$[A-Za-z_{])", r"\1 \2", text)
    text = re.sub(r"(\$[A-Za-z_][A-Za-z0-9_.]*)([\u0590-\u05ff])", r"\1 \2", text)
    return text


