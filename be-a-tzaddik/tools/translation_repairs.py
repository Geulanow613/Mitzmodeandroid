def apply_ru_religious_translit(text: str) -> str:
    """Transliterate Latin-script religious terms to Cyrillic."""
    for pat, repl in RU_RELIGIOUS_TERMS:
        text = re.sub(pat, repl, text)
    # Argos often leaves Latin h before Cyrillic vowels in ha- prefixes.
    text = re.sub(r"\bh(?=[а-яА-ЯёЁ])", "х", text)
    text = re.sub(r"guardar \(Шаббат\)", "«шамор» (Шаббат)", text)
    text = re.sub(r"guardar \(Shabbat\)", "«shamor» (Шаббат)", text)
    text = re.sub(r"\bРош Hashanah\b", "Рош а-Шана", text)
    text = re.sub(r"\bШушan\b", "Шушан", text)
    text = re.sub(r"\bгород сo\b", "город со", text)
    text = re.sub(r"\byour\b", "ваш", text, flags=re.IGNORECASE)
    text = re.sub(r"\bmitzvot\b", "мицвот", text, flags=re.IGNORECASE)
    text = re.sub(r"\bmitzv\b", "мицв", text, flags=re.IGNORECASE)
    text = re.sub(r"\bTorah authorities\b", "тора-авторитеты", text, flags=re.IGNORECASE)
