# Argos/shard typo: U+05F7 (׷) used instead of gershayim U+05F4 (״) in Hebrew numerals.
HE_GLYPH_STRING_FIXES: list[tuple[str, str]] = [
    ("י׷ב בתמוז", "י״ז בתמוז"),
    ("תענית י׷ב בתמוז", "תענית י״ז בתמוז"),
    ("תענית י׷ב בתבֵת", "תענית י׳ בטבת"),
]


def _repair_he_glyphs(text: str) -> str:
    for old, new in HE_GLYPH_STRING_FIXES:
        text = text.replace(old, new)
    # Catch any remaining qamats-punctuation between Hebrew letters (corrupt gershayim).
    text = re.sub(
        r"([\u05d0-\u05ea])\u05f7([\u05d0-\u05ea])",
        lambda m: m.group(1) + "\u05f4" + m.group(2),
        text,
    )
    return text


def _fix_ru_a_dot(text: str) -> str:
    for word, repl in RU_A_DOT.items():
        text = text.replace(f"А.{word}", repl)
    text = re.sub(r"А\.([a-zA-Z])", r"\1", text)
    return text


