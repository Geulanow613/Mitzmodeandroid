"""Generate base64-encoded Hebrew strings for he_fix 002/003."""
import base64
import json

def u(*codes):
    return "".join(chr(c) for c in codes)

def b64(s):
    return base64.b64encode(s.encode("utf-8")).decode("ascii")

def dec(b):
    return base64.b64decode(b).decode("utf-8")

