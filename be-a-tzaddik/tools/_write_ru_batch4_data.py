def main() -> int:
    keys_data = json.loads(KEYS_PATH.read_text(encoding="utf-8"))
    entries: dict[str, str] = {}
    missing: list[int] = []
    for item in keys_data:
        rank = item["rank"]
        key = item["key"]
        if rank not in TRANSLATIONS:
            missing.append(rank)
            continue
        entries[key] = TRANSLATIONS[rank]
    if missing:
        raise SystemExit(f"missing translations for ranks: {missing}")
    lines = [
        '"""Batch 4: top 50 worst Latin-hit keys — full native Cyrillic rewrites."""',
        "",
        "from __future__ import annotations",
        "",
        "TOP50_BATCH4: dict[str, str] = {",
    ]
    for key, val in entries.items():
        lines.append(f"    {key!r}: (")
        for part in val.split("\n"):
            lines.append(f"        {part!r}")
        lines.append("    ),")
    lines.append("}")
    lines.append("")
    OUT_PATH.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"wrote {len(entries)} keys to {OUT_PATH}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
