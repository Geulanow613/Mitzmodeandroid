def netilat_ru_for_key(key: str) -> str | None:
    if not key.startswith("Immediately after saying Modeh Ani"):
        return None
    for marker, section in NETILAT_RU_MINHAG.items():
        if marker in key:
            return NETILAT_RU_HEAD + section + NETILAT_RU_TAIL
    return None
